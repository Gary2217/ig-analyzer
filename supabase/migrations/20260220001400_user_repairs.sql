create table if not exists public.user_repairs (
  id bigint generated by default as identity primary key,
  user_id uuid not null,
  action text not null,
  ok boolean not null default true,
  details jsonb,
  created_at timestamptz not null default now()
);

create index if not exists user_repairs_user_created_idx
  on public.user_repairs (user_id, created_at desc);

alter table public.user_repairs enable row level security;

drop policy if exists "read own repairs" on public.user_repairs;
create policy "read own repairs"
on public.user_repairs
for select to authenticated
using (auth.uid() = user_id);

drop policy if exists "service role insert repairs" on public.user_repairs;
create policy "service role insert repairs"
on public.user_repairs
for insert to service_role
with check (true);
